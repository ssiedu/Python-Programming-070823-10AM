a=100
b=200
print("value of a={} \nvalue of b={}".format(a,b))
print("{} value of a \n{} value of b".format(b,a))
#with indexing
print("value of a={0} \nvalue of b={1}".format(a,b))
print("value of a={1} \nvalue of b={0}".format(a,b))
#with Key

print("value of a={y} \nvalue of b={x}".format(x=200,y=500))
print("value of a={a} \nvalue of b={b}".format(a=a,b=b))
